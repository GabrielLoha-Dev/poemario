  let filteredPoems = [];
  let currentIndex = 0;
  
  document.getElementById("search-form").addEventListener("submit", (e) => {
    e.preventDefault();
    const input = document.getElementById("poem-input").value.toLowerCase().trim();
    
    if (!input) {
      alert("Por favor, insira o número, título ou palavra-chave de um poema!");
      return;
    }
  
    if (!isNaN(input)) {
      filteredPoems = poems.filter(p => p.number === parseInt(input));
    } else if (input) {
      filteredPoems = poems.filter(p => 
        p.title.toLowerCase().includes(input) || 
        p.text.toLowerCase().includes(input)
      );
    }
  
    if (filteredPoems.length > 0) {
      currentIndex = 0;
      displayPoem(filteredPoems[currentIndex]);
      updateNavigation();
    } else {
      alert("Nenhum poema encontrado!");
      document.getElementById("poem-data").innerHTML = "";
      document.getElementById("navigation").style.display = "none";
    }
  });
  
  function displayPoem(poem) {
    const poemDataDiv = document.getElementById("poem-data");
    poemDataDiv.innerHTML = `
      <div class="poem-card">
        <h2>${poem.title} (Poema #${poem.number})</h2>
        <p>${poem.text}</p><br><br>
        <p>Assinado: Lua</p>
      </div>
    `;
  }
  
  function updateNavigation() {
    document.getElementById("navigation").style.display = filteredPoems.length > 1 ? "block" : "none";
  }
  
  document.getElementById("prev-btn").addEventListener("click", () => {
    if (currentIndex > 0) {
      currentIndex--;
      displayPoem(filteredPoems[currentIndex]);
    }
  });
  
  document.getElementById("next-btn").addEventListener("click", () => {
    if (currentIndex < filteredPoems.length - 1) {
      currentIndex++;
      displayPoem(filteredPoems[currentIndex]);
    }
  });
  